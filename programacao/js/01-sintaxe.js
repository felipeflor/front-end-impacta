//Comando de saída
console.log("Olá JavaScript");

/*Variáveis e constantes
var - variável de escopo global
let - variável de escopo global ou de bloco
const - constante de escopo global ou de bloco (precisa ser inicializada com valor)*/


//Exemplos
let ano = 2022;
const nome = "Felipe";

console.log(nome);
console.log(ano);

//Concatenação (tradicional)
console.log("O aluno " + nome + " está estudando JavaScript no ano de " + ano);

//Template literal/string (moderno)
console.log(`O aluno ${nome} está estudando JavaScript no ano de ${ano}`)

